//! Embedded bitmap fonts (ASCII 0x20–0x7E).
//!
//! Pixel fonts keep overlay text crisp over a game capture — they match the
//! look SNES emulator Lua authors (Mesen/BizHawk) expect. Each glyph row is a
//! byte; bit 0 is the leftmost pixel (LSB-first). Rendered as nearest-neighbor
//! quads by the painter so they never blur.
//!
//! Two faces:
//! * [`Font::Small`] — a compact 5x7 font. The default; dense-HUD friendly.
//! * [`Font::Normal`] — the classic public-domain 8x8 ("font8x8 basic").
//!
//! Per-glyph row count differs, so callers iterate `font.height()` rows and
//! step `font.advance()` pixels horizontally.

/// Selectable typeface. Scripts choose via `gfx.font("small"|"normal")`.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum Font {
    /// 5x7 compact (default).
    #[default]
    Small,
    /// 8x8 classic.
    Normal,
}

impl Font {
    pub fn parse(s: &str) -> Font {
        match s.to_ascii_lowercase().as_str() {
            "normal" | "big" | "large" | "8x8" => Font::Normal,
            _ => Font::Small, // "small", "5x7", anything else
        }
    }

    /// Glyph cell width in font-pixels.
    pub fn width(self) -> usize {
        match self {
            Font::Small => 5,
            Font::Normal => 8,
        }
    }

    /// Glyph cell height in font-pixels.
    pub fn height(self) -> usize {
        match self {
            Font::Small => 7,
            Font::Normal => 8,
        }
    }

    /// Horizontal advance (cell width + 1px spacing so chars don't touch).
    pub fn advance(self) -> usize {
        self.width() + 1
    }

    /// Vertical advance for `\n` (cell height + 1px line gap).
    pub fn line_advance(self) -> usize {
        self.height() + 1
    }

    /// Row bytes for `ch` (length == `self.height()`), falling back to '?'
    /// for anything outside printable ASCII.
    pub fn glyph(self, ch: char) -> &'static [u8] {
        let idx = {
            let c = ch as usize;
            if (0x20..=0x7E).contains(&c) {
                c - 0x20
            } else {
                ('?' as usize) - 0x20
            }
        };
        match self {
            Font::Small => &FONT_5X7[idx],
            Font::Normal => &FONT_8X8[idx],
        }
    }
}

